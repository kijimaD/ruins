// Package aiinput provides AI entity behavior processing and state transition logic.
//
// このパッケージは以下の責務を持つ：
//
// # 責務
//   - AIエンティティの行動決定ロジック
//   - 状態遷移システム（待機 -> 移動 -> 追跡 -> 待機）
//   - 視界判定とプレイヤー検出
//   - アクション計画（移動、追跡、待機）
//   - ターンベースの状態管理
//
// # 仕様
//   - CDDA風のターンベース状態遷移
//   - 視界距離によるプレイヤー検出
//   - ランダム要素を含む自然な行動パターン
//   - Actionシステムとの統合
//
// # 使い分け
//   - Processor: AIシステム全体の処理制御
//   - StateMachine: 状態遷移ロジック
//   - ActionPlanner: 各状態でのアクション決定
//   - VisionSystem: 視界判定
//
// # 設計思想
//   - 単一責任原則：各コンポーネントは明確な責務を持つ
//   - 依存性逆転：インターフェースを通じた疎結合
//   - テスト容易性：モック可能な設計
//   - 拡張性：新しい状態やアクションの追加が容易
