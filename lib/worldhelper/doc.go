// Package worldhelper はワールド操作のためのヘルパー関数を提供する。
//
// ## 主要機能
//
// ### ソート機能 (sort.go)
// - 汎用的なエンティティソート機能
// - SortableEntityインターフェースによる統一的なソート処理
// - 各種リスト（レシピ、素材、アイテム）のソート対応
// - 現在は名前順ソート、将来的にランク→名前順ソートに拡張予定
//
// ### クエリ機能 (query.go)
// - 所有素材の検索
// - プレイヤーの検索
//
// ### 合成機能 (craft.go)
// - レシピによる素材合成
// - 素材量の管理
//
// ### 装備管理 (equipment.go)
// - 装備・装備解除の処理
// - 装備によるステータス変更
//
// ### スポーン機能 (spawn.go)
// - エンティティの生成と初期化
// - HPやSPの最大値設定
//
// ## 設計方針
//
// - 各機能は単一責任の原則に従い、適切に分離
// - インターフェースを活用した拡張性の確保
// - テストしやすい設計の採用
// - パフォーマンスを考慮した実装
